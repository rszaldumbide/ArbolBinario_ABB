    private void btnGenerarRandomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarRandomActionPerformed
        // TODO add your handling code here:
        try {
            ABB = new arbolBinario_ABB();
            ABB2= new ABB_metodo_reflexivo();
            arbol = this.pnlGrafico.getGraphics();
            arbol2 = this.pnl2.getGraphics();

            int cantidad = Integer.parseInt(txtCantidad.getText());
            int numero;
            for (int i = 0; i < cantidad; i++) {
                //genera nums aleatorios desde el -10 al 20
                numero = (int) (Math.random() * 99 + 50);
                ABB.insertar(numero);
                ABB2.insertarReflexivo(numero);
            }
            graficarArbol(arbol, ABB.getRaiz(), x, y, x2, y2, 1);
            graficarArbol(arbol2, ABB2.getRaiz(), x, y, x2, y2, 1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtCantidad.setText("");
    }//GEN-LAST:event_btnGenerarRandomActionPerformed

